// With adds key-value pairs to the error context.
//
// Example:
//
//	oops.With("user_id", 123, "operation", "create").Errorf("validation failed")
func (b OopsErrorBuilder) With(kv ...any) OopsErrorBuilder {
	// Parse and snapshot at call time so later mutations to kv don't affect us.
	snap := make(map[string]any, len(kv)/2)
	for i := 0; i+1 < len(kv); i += 2 {
		if key, ok := kv[i].(string); ok {
			snap[key] = kv[i+1]
		}
	}
	prev := b.head
	return OopsErrorBuilder{head: func(e *OopsError, next optionFunc) {
		if prev != nil {
			prev(e, next)
		} else if next != nil {
			next(e, nil)
		}
		// Always merge (never direct-assign snap) so each materialization gets
		// its own e.context and snap remains read-only across multiple Wrap calls.
		if e.context == nil {
			e.context = make(map[string]any, len(snap))
		}
		for k, v := range snap {
			e.context[k] = v
		}
	}}
}

// WithContext extracts values from a Go context and adds them to the error context.
//
// Example:
//
//	oops.WithContext(ctx, "request_id", "user_id").Errorf("operation failed")
func (b OopsErrorBuilder) WithContext(ctx context.Context, keys ...any) OopsErrorBuilder {
	// Resolve all values and the OTel span at call time — context is immutable.
	snap := make(map[string]any, len(keys))
	for _, k := range keys {
		switch k := k.(type) {
		case fmt.Stringer:
			snap[k.String()] = contextValueOrNil(ctx, k.String())
		case string:
			snap[k] = contextValueOrNil(ctx, k)
		case *string:
			snap[*k] = contextValueOrNil(ctx, *k)
		default:
			snap[fmt.Sprint(k)] = contextValueOrNil(ctx, k)
		}
	}

	var otelTrace, otelSpan string
	spanCtx := trace.SpanContextFromContext(ctx)
	if spanCtx.HasTraceID() {
		otelTrace = spanCtx.TraceID().String()
	}
	if spanCtx.HasSpanID() {
		otelSpan = spanCtx.SpanID().String()
	}

	prev := b.head
	return OopsErrorBuilder{head: func(e *OopsError, next optionFunc) {
		if prev != nil {
			prev(e, next)
		} else if next != nil {
			next(e, nil)
		}
		if e.context == nil {
			e.context = make(map[string]any, len(snap))
		}
		for k, v := range snap {
			e.context[k] = v
		}
		if otelTrace != "" {
			e.trace = otelTrace
		}
		if otelSpan != "" {
			e.span = otelSpan
		}
	}}
}

