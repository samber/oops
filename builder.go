const badKey = "!BADKEY"

// identityArgsToMap converts variadic user/tenant arguments into a flat map.
//
// Supported inputs:
//   - slog.Attr values
//   - map[string]any values
//   - alternating string key/value pairs
//
// Malformed values are stored under "!BADKEY", mirroring log/slog argsToAttr.
func identityArgsToMap(args []any) map[string]any {
	payload := map[string]any{}

	for len(args) > 0 {
		switch value := args[0].(type) {
		case slog.Attr:
			payload[value.Key] = slogValueToAny(value.Value, 0)
			args = args[1:]
		case map[string]any:
			for key, mapValue := range value {
				payload[key] = mapValue
			}
			args = args[1:]
		case string:
			if len(args) == 1 {
				payload[badKey] = value
				return payload
			}

			payload[value] = args[1]
			args = args[2:]
		default:
			payload[badKey] = value
			args = args[1:]
		}
	}

	return payload
}

// slogValueToAny converts a slog.Value into its resolved Go representation.
//
// Group values are converted recursively into map[string]any so they can be
// stored in user/tenant payload maps.
//
// The depth parameter guards against circular slog.LogValuer chains.
// Returns nil when depth exceeds 10.
func slogValueToAny(value slog.Value, depth int) any {
	value = value.Resolve()

	switch value.Kind() {
	case slog.KindString:
		return value.String()
	case slog.KindInt64:
		return value.Int64()
	case slog.KindUint64:
		return value.Uint64()
	case slog.KindFloat64:
		return value.Float64()
	case slog.KindBool:
		return value.Bool()
	case slog.KindDuration:
		return value.Duration()
	case slog.KindTime:
		return value.Time()
	case slog.KindGroup:
		group := map[string]any{}
		for _, attr := range value.Group() {
			group[attr.Key] = slogValueToAny(attr.Value, depth+1)
		}
		return group
	case slog.KindLogValuer:
		if depth > 10 {
			return nil
		}
		return slogValueToAny(value.LogValuer().LogValue(), depth+1)
	case slog.KindAny:
		return value.Any()
	default:
		return value.Any()
	}
}

// CallerSkip sets the number of additional callers to skip when capturing
// the stack trace.
func (b OopsErrorBuilder) CallerSkip(skip int) OopsErrorBuilder {
	if skip < 0 {
		skip = 0
	}
	prev := b.head
	return OopsErrorBuilder{head: func(e *OopsError, next optionFunc) {
		if prev != nil {
			prev(e, next)
		} else if next != nil {
			next(e, nil)
		}
		e.callerSkip = skip
	}}
}

