// Join combines multiple errors into a single error.
//
// Example:
//
//	err := oops.
//	  Code("multi_error").
//	  Join(err1, err2, err3)
func (b OopsErrorBuilder) Join(e ...error) error {
	return b.Wrap(errors.Join(e...))
}

// Recover handles panics and converts them to OopsError instances.
//
// Example:
//
//	err := oops.
//	  Code("panic_recovered").
//	  Recover(func() { riskyOperation() })
func (b OopsErrorBuilder) Recover(cb func()) (err error) {
	defer func() {
		if r := recover(); r != nil {
			if e, ok := r.(error); ok {
				err = b.Wrap(e)
			} else {
				err = b.Wrap(fmt.Errorf("%v", r))
			}
		}
	}()

	cb()
	return err
}

// Recoverf handles panics with additional context message.
//
// Example:
//
//	err := oops.
//	  Code("panic_recovered").
//	  Recoverf(func() { riskyOperation() }, "panic in operation: %s", operationName)
func (b OopsErrorBuilder) Recoverf(cb func(), msg string, args ...any) (err error) {
	return b.Wrapf(b.Recover(cb), msg, args...)
}

// Assert panics if the condition is false.
//
// Example:
//
//	oops.
//	  Code("assertion_failed").
//	  Assert(userID != "")
func (b OopsErrorBuilder) Assert(condition bool) OopsErrorBuilder {
	if !condition {
		panic(b.Errorf("assertion failed"))
	}

	return b
}

// Assertf panics if the condition is false with a custom message.
//
// Example:
//
//	oops.
//	  Code("assertion_failed").
//	  Assertf(userID != "", "user ID cannot be empty, got: %s", userID)
func (b OopsErrorBuilder) Assertf(condition bool, msg string, args ...any) OopsErrorBuilder {
	if !condition {
		panic(b.Errorf(msg, args...))
	}

	return b
}

