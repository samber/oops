// Format implements the fmt.Formatter interface for custom formatting.
// Supports the following format verbs:
// - %v: standard error message
// - %+v: verbose format with stack trace and context
// - %#v: Go syntax representation.
func (o OopsError) Format(s fmt.State, verb rune) {
	switch verb {
	case 'v':
		if s.Flag('+') {
			// Verbose format with stack trace and context
			_, _ = fmt.Fprint(s, o.formatVerbose())
		} else {
			// Standard format
			_, _ = fmt.Fprint(s, o.formatSummary())
		}
	case 's':
		_, _ = fmt.Fprint(s, o.Error())
	case 'q':
		_, _ = fmt.Fprintf(s, "%q", o.Error())
	default:
		_, _ = fmt.Fprint(s, o.formatSummary())
	}
}

// formatVerbose returns a detailed string representation of the error
// including all context, stack trace, and source code fragments.
func (o *OopsError) formatVerbose() string { //nolint:gocyclo
	s := snapshot(*o)

	var output strings.Builder
	_, _ = fmt.Fprintf(&output, "Oops: %s\n", o.Error())

	if s.code != nil {
		_, _ = fmt.Fprintf(&output, "Code: %v\n", s.code)
	}

	if s.time != (time.Time{}) {
		_, _ = fmt.Fprintf(&output, "Time: %s\n", s.time.In(Local))
	}

	if s.duration != 0 {
		_, _ = fmt.Fprintf(&output, "Duration: %s\n", s.duration.String())
	}

	if s.domain != "" {
		_, _ = fmt.Fprintf(&output, "Domain: %s\n", s.domain)
	}

	if len(s.tags) > 0 {
		_, _ = fmt.Fprintf(&output, "Tags: %s\n", strings.Join(s.tags, ", "))
	}

	if s.trace != "" {
		_, _ = fmt.Fprintf(&output, "Trace: %s\n", s.trace)
	}

	// if span := o.Span(); span != "" {
	// 	_, _ = fmt.Fprintf(&output, "Span: %s\n", span)
	// }

	if s.hint != "" {
		_, _ = fmt.Fprintf(&output, "Hint: %s\n", s.hint)
	}

	if s.owner != "" {
		_, _ = fmt.Fprintf(&output, "Owner: %s\n", s.owner)
	}

	if len(s.context) > 0 {
		output.WriteString("Context:\n")
		for k, v := range s.context {
			_, _ = fmt.Fprintf(&output, "  * %s: %v\n", k, v)
		}
	}

	if s.userID != "" || len(s.userData) > 0 {
		output.WriteString("User:\n")
		if s.userID != "" {
			_, _ = fmt.Fprintf(&output, "  * id: %s\n", s.userID)
		}
		for k, v := range s.userData {
			_, _ = fmt.Fprintf(&output, "  * %s: %v\n", k, v)
		}
	}

	if s.tenantID != "" || len(s.tenantData) > 0 {
		output.WriteString("Tenant:\n")
		if s.tenantID != "" {
			_, _ = fmt.Fprintf(&output, "  * id: %s\n", s.tenantID)
		}
		for k, v := range s.tenantData {
			_, _ = fmt.Fprintf(&output, "  * %s: %v\n", k, v)
		}
	}

	if s.req != nil {
		dump, e := httputil.DumpRequestOut(s.req.A, s.req.B)
		if e == nil {
			output.WriteString("Request:\n")
			writePrefixedLines(&output, dump)
		}
	}

	if s.res != nil {
		dump, e := httputil.DumpResponse(s.res.A, s.res.B)
		if e == nil {
			output.WriteString("Response:\n")
			writePrefixedLines(&output, dump)
		}
	}

	if stacktrace := o.Stacktrace(); stacktrace != "" {
		lines := strings.Split(stacktrace, "\n")
		stacktrace = "  " + strings.Join(lines, "\n  ")
		_, _ = fmt.Fprintf(&output, "Stacktrace:\n%s\n", stacktrace)
	}

	if sources := o.Sources(); sources != "" && !SourceFragmentsHidden {
		_, _ = fmt.Fprintf(&output, "Sources:\n%s\n", sources)
	}

	return output.String()
}

// formatSummary returns a brief summary of the error for logging.
func (o *OopsError) formatSummary() string {
	return o.Error()
}

// writePrefixedLines writes s to output with each line prefixed by "  * ",
// followed by a trailing newline. Equivalent to splitting on "\n", prefixing
// every element (including a trailing empty one), and joining - but in a
// single pass without the intermediate slices.
//
// s is taken as []byte rather than string because both callers hold a
// httputil dump as []byte already; accepting []byte here avoids the
// string(dump) copy that a string parameter would force at the call site.
func writePrefixedLines(output *strings.Builder, s []byte) {
	for {
		output.WriteString("  * ")
		idx := bytes.IndexByte(s, '\n')
		if idx < 0 {
			output.Write(s)
			output.WriteByte('\n')
			return
		}
		output.Write(s[:idx])
		output.WriteByte('\n')
		s = s[idx+1:]
	}
}
