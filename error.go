// LogValue returns a slog.Value representation of the error for structured logging.
// This method implements the slog.LogValuer interface and provides a flattened
// representation of the error's context and metadata suitable for logging systems.
func (o OopsError) LogValue() slog.Value { //nolint:gocyclo
	s := snapshot(o)

	attrs := make([]slog.Attr, 0, 16)
	attrs = append(attrs, slog.String("message", o.msg))

	if err := o.Error(); err != "" {
		attrs = append(attrs, slog.String("err", err))
	}

	if s.code != nil {
		attrs = append(attrs, slog.Any("code", s.code))
	}

	if s.time != (time.Time{}) {
		attrs = append(attrs, slog.Time("time", s.time.In(Local)))
	}

	if s.duration != 0 {
		attrs = append(attrs, slog.Duration("duration", s.duration))
	}

	if s.domain != "" {
		attrs = append(attrs, slog.String("domain", s.domain))
	}

	if len(s.tags) > 0 {
		attrs = append(attrs, slog.Any("tags", s.tags))
	}

	if s.trace != "" {
		attrs = append(attrs, slog.String("trace", s.trace))
	}

	// if span := o.Span(); span != "" {
	// 	attrs = append(attrs, slog.String("span", span))
	// }

	if s.hint != "" {
		attrs = append(attrs, slog.String("hint", s.hint))
	}

	if s.public != "" {
		attrs = append(attrs, slog.String("public", s.public))
	}

	if s.owner != "" {
		attrs = append(attrs, slog.String("owner", s.owner))
	}

	if len(s.context) > 0 {
		args := make([]any, 0, len(s.context))
		for k, v := range s.context {
			args = append(args, slog.Any(k, v))
		}
		attrs = append(attrs, slog.Group("context", args...))
	}

	if s.userID != "" || len(s.userData) > 0 {
		userPayload := make([]any, 0, len(s.userData)+1)
		if s.userID != "" {
			userPayload = append(userPayload, slog.String("id", s.userID))
			for k, v := range s.userData {
				userPayload = append(userPayload, slog.Any(k, v))
			}
		}
		attrs = append(attrs, slog.Group("user", userPayload...))
	}

	if s.tenantID != "" || len(s.tenantData) > 0 {
		tenantPayload := make([]any, 0, len(s.tenantData)+1)
		if s.tenantID != "" {
			tenantPayload = append(tenantPayload, slog.String("id", s.tenantID))
			for k, v := range s.tenantData {
				tenantPayload = append(tenantPayload, slog.Any(k, v))
			}
		}
		attrs = append(attrs, slog.Group("tenant", tenantPayload...))
	}

	if s.req != nil {
		dump, e := httputil.DumpRequestOut(s.req.A, s.req.B)
		if e == nil {
			attrs = append(attrs, slog.String("request", string(dump)))
		}
	}

	if s.res != nil {
		dump, e := httputil.DumpResponse(s.res.A, s.res.B)
		if e == nil {
			attrs = append(attrs, slog.String("response", string(dump)))
		}
	}

	if stacktrace := o.Stacktrace(); stacktrace != "" {
		attrs = append(attrs, slog.String("stacktrace", stacktrace))
	}

	if sources := o.Sources(); sources != "" && !SourceFragmentsHidden {
		attrs = append(attrs, slog.String("sources", sources))
	}

	return slog.GroupValue(attrs...)
}

// ToMap converts the error to a map representation suitable for JSON serialization.
// This method provides a flattened view of all error attributes and is useful
// for logging, debugging, and cross-service error transmission.
func (o OopsError) ToMap() map[string]any { //nolint:gocyclo
	s := snapshot(o)
	payload := map[string]any{}

	if err := o.Error(); err != "" {
		payload["error"] = err
	}

	if s.code != nil {
		payload["code"] = s.code
	}

	if s.time != (time.Time{}) {
		payload["time"] = s.time.In(Local)
	}

	if s.duration != 0 {
		payload["duration"] = s.duration.String()
	}

	if s.domain != "" {
		payload["domain"] = s.domain
	}

	if len(s.tags) > 0 {
		payload["tags"] = s.tags
	}

	if len(s.context) > 0 {
		payload["context"] = s.context
	}

	if s.trace != "" {
		payload["trace"] = s.trace
	}

	// if span := o.Span(); span != "" {
	// 	payload["span"] = span
	// }

	if s.hint != "" {
		payload["hint"] = s.hint
	}

	if s.public != "" {
		payload["public"] = s.public
	}

	if s.owner != "" {
		payload["owner"] = s.owner
	}

	if s.userID != "" || len(s.userData) > 0 {
		// snapshot() owns s.userData (freshly merged in mergeMaps), so it can
		// be used directly instead of copying.
		user := s.userData
		if s.userID != "" {
			user["id"] = s.userID
		}
		payload["user"] = user
	}

	if s.tenantID != "" || len(s.tenantData) > 0 {
		// snapshot() owns s.tenantData (freshly merged in mergeMaps), so it can
		// be used directly instead of copying.
		tenant := s.tenantData
		if s.tenantID != "" {
			tenant["id"] = s.tenantID
		}
		payload["tenant"] = tenant
	}

	if s.req != nil {
		dump, e := httputil.DumpRequestOut(s.req.A, s.req.B)
		if e == nil {
			payload["request"] = string(dump)
		}
	}

	if s.res != nil {
		dump, e := httputil.DumpResponse(s.res.A, s.res.B)
		if e == nil {
			payload["response"] = string(dump)
		}
	}

	if stacktrace := o.Stacktrace(); stacktrace != "" {
		payload["stacktrace"] = stacktrace
	}

	if sources := o.Sources(); sources != "" && !SourceFragmentsHidden {
		payload["sources"] = sources
	}

	return payload
}

